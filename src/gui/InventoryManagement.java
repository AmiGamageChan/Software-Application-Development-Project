    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String name = jTextField1.getText();
        String price = jTextField2.getText();
        String desc = jTextField3.getText();
        String type = (String) jComboBox1.getSelectedItem();

        if (name.isEmpty()) {
            Notifications.getInstance().show(Notifications.Type.WARNING, Notifications.Location.TOP_CENTER, "Please enter item name");
        } else if (price.isEmpty()) {
            Notifications.getInstance().show(Notifications.Type.WARNING, Notifications.Location.TOP_CENTER, "Please enter the price");
        } else if (desc.isEmpty()) {
            Notifications.getInstance().show(Notifications.Type.WARNING, Notifications.Location.TOP_CENTER, "Please enter a description");
        } else if (type.equals("Select")) {
            Notifications.getInstance().show(Notifications.Type.WARNING, Notifications.Location.TOP_CENTER, "please select the item type");
        } else {
            try {
                SQL.executeIUD("INSERT INTO `menu_item` (`name`,`price`,`item_type_id`,`description`) VALUES ('" + name + "','" + price + "','" + itemTypeMap.get(type) + "','" + desc + "')");

                Notifications.getInstance().show(Notifications.Type.SUCCESS, Notifications.Location.TOP_CENTER, "Menu Item Registered");

                loadTable();
                reset();
                logger.info("A menu item registered");
            } catch (Exception e) {
                e.printStackTrace();
                logger.severe("SQL Exception");
            }
        }


    }//GEN-LAST:event_jButton1ActionPerformed

