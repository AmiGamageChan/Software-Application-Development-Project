    private void loadTable() {
        String searchText = jTextField4.getText().trim();
        String selectedSort = (String) jComboBox2.getSelectedItem();
        try {
            DefaultTableCellRenderer centerRenderer = new DefaultTableCellRenderer();
            centerRenderer.setHorizontalAlignment(JLabel.CENTER);
            DefaultTableModel dtm = (DefaultTableModel) jTable2.getModel();

            dtm.setRowCount(0);
            String query = "SELECT * FROM `menu_item` INNER JOIN `item_type` ON `menu_item`.`item_type_id`=`item_type`.`id`";
            if (!searchText.isEmpty()) {
                query += " WHERE `menu_item`.`name` LIKE '%" + searchText + "%'";
            }
            if ("Price Ascending".equals(selectedSort)) {
                query += " ORDER BY `menu_item`.`price` ASC";
            } else if ("Price Descending".equals(selectedSort)) {
                query += " ORDER BY `menu_item`.`price` DESC";
            }
            ResultSet result = SQL.executeSearch(query);

            while (result.next()) {
                Vector<String> vector = new Vector<>();

                vector.add(result.getString("id"));
                vector.add(result.getString("name"));
                vector.add(result.getString("price"));
                vector.add(result.getString("item_type.name"));
                vector.add(result.getString("description"));

                dtm.addRow(vector);

            }
            for (int i = 0; i < jTable2.getColumnModel().getColumnCount(); i++) {
                jTable2.getColumnModel().getColumn(i).setCellRenderer(centerRenderer);
            }
        } catch (Exception e) {
            e.printStackTrace();
            logger.warning("Table load error");
        }
    }

    private void loadComboBox() {
        try {
            ResultSet result = SQL.executeSearch("SELECT * FROM `item_type`");
            Vector<String> vector = new Vector<>();
            vector.add("Select");
            while (result.next()) {
                vector.add(result.getString("name"));
                itemTypeMap.put(result.getString("name"), result.getString("id"));
            }

            DefaultComboBoxModel dcm = new DefaultComboBoxModel(vector);
            jComboBox1.setModel(dcm);

        } catch (Exception e) {
            e.printStackTrace();
            logger.severe("Loading combo box failed");
        }
    }

